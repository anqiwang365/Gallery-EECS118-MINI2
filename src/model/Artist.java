package model;

public class Artist {
	
	public int aId;
	public String name;
	public int birth;
	public String country;
	public String aDes;
	
	
	public int getaId() {
		return aId;
	}
	public void setaId(int aId) {
		this.aId = aId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getBirth() {
		return birth;
	}
	public void setBirth(int birth) {
		this.birth = birth;
	}
	public String getCountry() {
		return country;
	}
	public void setCountry(String country) {
		this.country = country;
	}
	public String getaDes() {
		return aDes;
	}
	public void setaDes(String description) {
		this.aDes = description;
	}
	
	
	

}
